public class CicloWhile {
    
    private Integer identificadorIf;
    private String idColor;
    private String idImagen;
    private Integer[] rangoX = new Integer[2];
    private Integer[] rangoY = new Integer[2];
    private String posX;
    private String posY;

    public Integer getIdentificadorIf() {
        return identificadorIf;
    }

    public void setIdentificadorIf(Integer identificadorIf) {
        this.identificadorIf = identificadorIf;
    }

    public String getIdColor() {
        return idColor;
    }

    public void setIdColor(String idColor) {
        this.idColor = idColor;
    }

    public String getIdImagen() {
        return idImagen;
    }

    public void setIdImagen(String idImagen) {
        this.idImagen = idImagen;
    }

    public Integer[] getRangoX() {
        return rangoX;
    }

    public void setRangoX(Integer[] rangoX) {
        this.rangoX = rangoX;
    }

    public Integer[] getRangoY() {
        return rangoY;
    }

    public void setRangoY(Integer[] rangoY) {
        this.rangoY = rangoY;
    }

    public String getPosX() {
        return posX;
    }

    public void setPosX(String posX) {
        this.posX = posX;
    }

    public String getPosY() {
        return posY;
    }

    public void setPosY(String posY) {
        this.posY = posY;
    }
    
    
    
}
